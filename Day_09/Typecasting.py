a = "1"
b = "2"

print(a + b)

a = 1
b = 2
print(a + b)

c = "10"
d = "10"
print(int(c) + int(d))

# Explicit Typecasting => done by developer or programmer

# Implicit Typecasting => python interpreter does it automatically
e = 1.9
f = 5
print(e + f) # implicit